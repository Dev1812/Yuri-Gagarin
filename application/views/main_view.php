	  	  В 1959 году Гагарин дослужил до чина старшего лейтенанта, получив звание военного лётчика 1-го класса. Тогда же на государственном уровне было закреплено постановление о поиске и отборе кандидатов на полёт за пределы Земли. Услышав об этом, летчик написал руководству рапорт, в котором просил зачислить его в кандидаты. После многочисленных проверок Гагарина утвердили как одного из 20 предполагаемых будущих космонавтов. В марте 1960 года он приступил к тренировкам.

Несмотря на жесткую конкуренцию, Гагарин сумел завоевать симпатии даже своих соперников. Надёжный, сильный и доброжелательный юноша никому не завидовал, никого не считал лучше или хуже себя, и это было видно по его поведению и манере речи. Космонавт легко брал на себя инициативу, работал упорно и с удовольствием.

