	  <div class="paragraph">
	  Полёт на корабле "Восток" был сопряжен с огромными рисками для жизни Гагарина. По причине спешки не были продублированы некоторые важные системы, корабль не оборудовался системой мягкой посадки, даже не была создана система аварийного спасения на случай неполадок во время старта. Шанс, что первый космонавт погибнет, так и не поднявшись в воздух, был очень большим.

12 апреля 1961 года осуществился взлёт с космодрома "Байконур". Все знают эту легендарную фразу "Поехали!". Из-за того, что возникли неполадки с оборудованием, Гагарин поднялся на 100 километров выше, чем изначально планировалось. Если бы возникли проблемы с тормозной установкой, космонавту пришлось бы возвращаться на Землю больше месяца, при этом запас воды и пищи был рассчитан всего на 10 дней.



Несмотря на многочисленные проблемы Юрий Гагарин благополучно спустился со атмосферы. Его аппарат приземлился не там, где планировалось, а потому космонавта довезли до ближайшего посёлка, и уже оттуда позвонил начальству с докладом об успешном приземлении и отсутствии травм. Поскольку полёт был секретным, даже советские СМИ узнали о технологическом прорыве родного государства только на следующий день.

Как только информация стала доступна, Гагарин превратился в звезду всемирного масштаба. Этому немало поспособствовал Хрущёв, настоявший на достойном приёме героя. 14 апреля состоялось грандиозное празднество в честь космонавта.

Через месяц Гагарина отправили за границу, где ему предстояло посетить более 20 стран, работая в том числе и в дипломатическом ключе. Во всех этих поездках Гагарин зарекомендовал себя как тактичный и обаятельный молодой человек. Его личная харизма немало способствовала укреплению положительного имиджа СССР.
